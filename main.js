//NFL
function showNFLfeed() {
    const nflTwitter = document.getElementById('NFL-twit');
    if (nflTwitter.style.display === "none") {
      nflTwitter.style.display = "block";
    } else {
      nflTwitter.style.display = "none";
    }
  }
  
